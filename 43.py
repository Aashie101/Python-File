# Write a python program to demonstrate String Traversing using the for loop

def main():
    alphabet = "google"
    index = 0
    print(f"In the string '{alphabet}'")
    for char in alphabet:
        print(f"Character '{char}' has an index value of {index}")
        index += 1

if __name__ == "__main__":
    main()
